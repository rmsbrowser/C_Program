/**
 * Encode a single Unicode code point to a buffer.
 *
 * @param ch the code point to encode.
 * @param utf8_buffer the buffer to encode to.  There must be at least 4
 * bytes available.
 * @return the number of encoded bytes.
 */
int utf8_encode(const wchar_t ch, char * utf8_buffer) {
    if (ch <= 0x7F) {
        utf8_buffer[0] = (char) ch;
        return 1;
    }
    if (ch <= 0x7FF) {
        utf8_buffer[0] = 0xC0 | ((ch >> 6) & 0x1F);
        utf8_buffer[1] = 0x80 | (ch & 0x3F);
        return 2;
    }
    if (ch <= 0xFFFF) {
        utf8_buffer[0] = 0xE0 | ((ch >> 12) & 0x0F);
        utf8_buffer[1] = 0x80 | ((ch >> 6) & 0x3F);
        utf8_buffer[2] = 0x80 | (ch & 0x3F);
        return 3;
    }
    if (ch <= 0x10FFFF) {
        utf8_buffer[0] = 0xF0 | ((ch >> 18) & 0x07);
        utf8_buffer[1] = 0x80 | ((ch >> 12) & 0x3F);
        utf8_buffer[2] = 0x80 | ((ch >> 6) & 0x3F);
        utf8_buffer[3] = 0x80 | (ch & 0x3F);
        return 4;
    }
    /*
     * This isn't a defined UTF-8 character, just return blank
     */
    return 0;
}

