/**
 * Keyboard handler for the codepage selection dialog.
 *
 * @param keystroke the keystroke from the user.
 * @param flags KEY_FLAG_ALT, KEY_FLAG_CTRL, etc.  See input.h.
 */
void codepage_keyboard_handler(const int keystroke, const int flags) {
    Q_BOOL codepage_cp437 = Q_FALSE;
    Q_BOOL codepage_iso8859_1 = Q_FALSE;
    Q_BOOL codepage_cp720 = Q_FALSE;
    Q_BOOL codepage_cp737 = Q_FALSE;
    Q_BOOL codepage_cp775 = Q_FALSE;
    Q_BOOL codepage_cp850 = Q_FALSE;
    Q_BOOL codepage_cp852 = Q_FALSE;
    Q_BOOL codepage_cp857 = Q_FALSE;
    Q_BOOL codepage_cp858 = Q_FALSE;
    Q_BOOL codepage_cp860 = Q_FALSE;
    Q_BOOL codepage_cp862 = Q_FALSE;
    Q_BOOL codepage_cp863 = Q_FALSE;
    Q_BOOL codepage_cp866 = Q_FALSE;
    Q_BOOL codepage_cp1250 = Q_FALSE;
    Q_BOOL codepage_cp1251 = Q_FALSE;
    Q_BOOL codepage_cp1252 = Q_FALSE;
    Q_BOOL codepage_koi8_r = Q_FALSE;
    Q_BOOL codepage_koi8_u = Q_FALSE;

    /*
     * Default to CP437
     */
    Q_CODEPAGE new_codepage = Q_CODEPAGE_CP437;

    /*
     * Only enable a codepage for this emulation
     */
    switch (q_status.emulation) {
    case Q_EMUL_VT52:
    case Q_EMUL_VT100:
    case Q_EMUL_VT102:
    case Q_EMUL_VT220:
    case Q_EMUL_LINUX_UTF8:
    case Q_EMUL_XTERM_UTF8:
    case Q_EMUL_PETSCII:
    case Q_EMUL_ATASCII:
        break;

    case Q_EMUL_TTY:
    case Q_EMUL_DEBUG:
    case Q_EMUL_ANSI:
    case Q_EMUL_AVATAR:
    case Q_EMUL_LINUX:
    case Q_EMUL_XTERM:
        codepage_cp437 = Q_TRUE;
        codepage_iso8859_1 = Q_TRUE;
        codepage_cp720 = Q_TRUE;
        codepage_cp737 = Q_TRUE;
        codepage_cp775 = Q_TRUE;
        codepage_cp850 = Q_TRUE;
        codepage_cp852 = Q_TRUE;
        codepage_cp857 = Q_TRUE;
        codepage_cp858 = Q_TRUE;
        codepage_cp860 = Q_TRUE;
        codepage_cp862 = Q_TRUE;
        codepage_cp863 = Q_TRUE;
        codepage_cp866 = Q_TRUE;
        codepage_cp1250 = Q_TRUE;
        codepage_cp1251 = Q_TRUE;
        codepage_cp1252 = Q_TRUE;
        codepage_koi8_r = Q_TRUE;
        codepage_koi8_u = Q_TRUE;
        break;
    }

    switch (keystroke) {
    case 'A':
    case 'a':
        if (codepage_cp437 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP437;
        } else {
            return;
        }
        break;
    case 'B':
    case 'b':
        if (codepage_iso8859_1 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_ISO8859_1;
        } else {
            return;
        }
        break;
    case 'C':
    case 'c':
        if (codepage_cp720 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP720;
        } else {
            return;
        }
        break;
    case 'D':
    case 'd':
        if (codepage_cp737 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP737;
        } else {
            return;
        }
        break;
    case 'E':
    case 'e':
        if (codepage_cp775 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP775;
        } else {
            return;
        }
        break;
    case 'F':
    case 'f':
        if (codepage_cp850 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP850;
        } else {
            return;
        }
        break;
    case 'G':
    case 'g':
        if (codepage_cp852 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP852;
        } else {
            return;
        }
        break;
    case 'H':
    case 'h':
        if (codepage_cp857 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP857;
        } else {
            return;
        }
        break;
    case 'I':
    case 'i':
        if (codepage_cp858 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP858;
        } else {
            return;
        }
        break;
    case 'J':
    case 'j':
        if (codepage_cp860 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP860;
        } else {
            return;
        }
        break;
    case 'K':
    case 'k':
        if (codepage_cp862 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP862;
        } else {
            return;
        }
        break;
    case 'L':
    case 'l':
        if (codepage_cp863 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP863;
        } else {
            return;
        }
        break;
    case 'M':
    case 'm':
        if (codepage_cp866 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP866;
        } else {
            return;
        }
        break;
    case 'N':
    case 'n':
        if (codepage_cp1250 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP1250;
        } else {
            return;
        }
        break;
    case 'O':
    case 'o':
        if (codepage_cp1251 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP1251;
        } else {
            return;
        }
        break;
    case 'P':
    case 'p':
        if (codepage_cp1252 == Q_TRUE) {
            new_codepage = Q_CODEPAGE_CP1252;
        } else {
            return;
        }
        break;
    case 'Q':
    case 'q':
        if (codepage_koi8_r == Q_TRUE) {
            new_codepage = Q_CODEPAGE_KOI8_R;
        } else {
            return;
        }
        break;
    case 'R':
    case 'r':
        if (codepage_koi8_u == Q_TRUE) {
            new_codepage = Q_CODEPAGE_KOI8_U;
        } else {
            return;
        }
        break;

    case Q_KEY_F(1):
        launch_help(Q_HELP_CODEPAGE);

        /*
         * Refresh the whole screen.
         */
        console_refresh(Q_FALSE);
        q_screen_dirty = Q_TRUE;
        return;

    case '`':
        /*
         * Backtick works too
         */
    case Q_KEY_ESCAPE:
        /*
         * ESC return to TERMINAL mode
         */
        switch_state(Q_STATE_CONSOLE);

        /*
         * The ABORT exit point
         */
        return;

    default:
        /*
         * Ignore keystroke
         */
        return;
    }

    /*
     * Set new codepage
     */
    q_status.codepage = new_codepage;

    /*
     * The OK exit point
     */
    switch_state(Q_STATE_CONSOLE);
}

